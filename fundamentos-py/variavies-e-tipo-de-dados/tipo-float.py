"""
Tipo real, decimal ou float
Casas decimais
"." e nao "," => (OK)10.0 / (NOK)10,0
"""

valor = 1, 44
print(valor)
print(type(valor))

valor = 1.44
print(valor)
print(type(valor))

valor1, valor2 = 1, 44
print(valor1)
print(type(valor1))
print(valor2)
print(type(valor2))

res = int(valor)
print(res)
print(valor)
print(type(res))

var = 5j
print(type(var))
print(var**2)

